#include "../libmath.h"

double	dot_product(t_vec3 v1, t_vec3 v2)
{
	double	res;

	res = (v1.x * v2.x) + (v1.y * v2.y) + (v1.z * v2.z) + (v1.w * v2.w);
	return (res);
}
